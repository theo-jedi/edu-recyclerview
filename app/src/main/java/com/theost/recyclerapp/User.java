package com.theost.recyclerapp;

public class User {

    private int id;
    private String avatar;
    private String name;
    private String email;

    public User(int id, String avatar, String name, String email) {
        this.id = id;
        this.avatar = avatar;
        this.name = name;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

}
